from CryptoLab import invmod, gcd

m = list(map(int, input().split()))
c = list(map(int, input().split()))
data = []
for i in range(len(c)):
    n = m[i]
    msg = c[i]
    data = data + [(msg, n)]

for i in range(1, len(c)):
    t = gcd(m[i], m[i - 1])
    assert (c[i] - c[i - 1]) % t == 0
    c[i] = (invmod(m[i - 1] // t, m[i] // t) * (c[i] - c[i - 1]) // t) % (m[i] // t) * m[i - 1] + c[i - 1]
    m[i] = m[i] // t * m[i - 1]
    c[i] = (c[i] % m[i] + m[i]) % m[i]

ans = c[len(c) - 1]
print(ans)
for msg, n in data:
    assert ans % n == msg % n
